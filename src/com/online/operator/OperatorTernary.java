package com.online.operator;

//Block class
public class OperatorTernary {
	// Method main()
	public static void main(String[] args) {
		// Declare variable
		boolean suka = true;
		String jawaban;

		// menggunakan operator ternary
		jawaban = suka ? "iya" : "tidak";

		// menampilkan jawaban
		System.out.println(jawaban);

	}

}
