package com.online.cabangifelse;

// block class
public class WithTernary {
	// method
	public static void main(String[] args) {
		// declare variable
		boolean suka = true;
		String jawaban;

		// using ternary operator
		jawaban = suka ? "iya" : "tidak";

		// show answer
		System.out.println(jawaban);
	}
}
