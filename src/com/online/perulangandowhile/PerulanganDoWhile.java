package com.online.perulangandowhile;

// Block class
public class PerulanganDoWhile {
	// Method main
	public static void main(String[] args) {

		// Create/declare variable
		int i = 0;

		// block code do/while
		do {
			// Block code for looping
			System.out.println("perulangan ke-" + i);
			i++;
		} while (i <= 10);

	}

}
